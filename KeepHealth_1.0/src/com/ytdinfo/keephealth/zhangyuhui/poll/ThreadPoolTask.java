package com.ytdinfo.keephealth.zhangyuhui.poll;

import android.content.Context;

/**
 * @author zhangyuhui
 *	线程池启动线程的父类。所有的线程都要继承此类才可以进行管理。
 */
public abstract class ThreadPoolTask implements Runnable {
	public Context context;

	public ThreadPoolTask(Context context) {
		super();
		this.context = context;
	}

}
